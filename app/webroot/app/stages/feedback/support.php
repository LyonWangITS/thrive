<section>
	<h3>Meridian Behavioral Health Center</h3>
	<p>Meridian is a private, non-profit organization. They are committed to enhancing health and wellness for all those with whom they come in contact. Whether you are looking to improve your sense of well-being, gain a performance edge, or deal with a mental illness or substance use issue, they have staff and services to meet your needs. Visit them on the web at mbhci.org or call them at (800) 330-5615 or (352) 374-5615</p>
</section>

<section>
	<h3>Florida Recovery Center</h3>
	<p>Florida Recovery Center is a treatment center that is run by UF Health. They provide all levels of care for people suffering from drug addiction or alcohol addiction who need drug rehab or alcohol treatment. The staff can provide medical consults, comprehensive labs and psychological testing for people who have an addiction and/or other psychiatric conditions. Visit them on the web at <a href="floridarecoverycenter.ufhealth.org">floridarecoverycenter.ufhealth.org</a> or call them at (855) 265-4FRC</p>
</section>

