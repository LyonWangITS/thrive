<section>
	<h2>Stuff about alcohol and you...</h2>
	<p>Safe drinking guidelines have been developed to help you protect your health - both physically and socially. Everyone is faced with some risk when consuming alcohol but thinking before you drink can lead to an enjoyable and safe experience.</p>
</section>

<section>
	<h3>Standard Drinks</h3>
    <p>Alcohol comes in all different types, colours and containers. Regardless of this, a standard drink is about 14 grams or 17.7mL of pure alcohol. Alcohol labels are displayed on the bottle to tell you how many standard drinks it contains. Remember one bottle/can/glass doesn't always equal one standard drink.</p>
    <ul>
        <li>Men shouldn't drink more than 4 standard drinks a day to avoid long-term harm, with at least two alcohol-free days a week.</li>
        <li>Women shouldn't drink more than 3 standard drinks a day to avoid long-term harm, with at least two alcohol-free days a week.</li>
    </ul>
</section>

<section>
	<h3>Everyone is Different</h3>
	<p>People come in all different shapes and sizes, so depending on your gender, age, weight and other factors; you may be affected by alcohol more than others. Get to know your body in relation to the standard drink guidelines, and try to keep one to two days a week free of alcohol.<p>
</section>

<section>
	<h3>Staying Under the Limit</h3>
	<p>It doesn't take much to put you over the 0.08% blood alcohol concentration (BAC) limit (0.02% for those under the age of 21). To stay under this:</p>
	<ul>
		<li>Women of average size shouldn't drink more than one standard drink per hour; and</li>
		<li>Men of average size can drink up to two standard drinks in the first hour and no more than one standard drink per hour after that.</li>
	</ul>
</section>

<section>
	<h3>Alcohol and Your Body</h3>
	<p>The immediate effects of alcohol can include slurred speech, blurred vision, changes in mood, loss of inhibitions, vomiting, loss of balance and clumsiness. These effects are greater with increasing amounts of alcohol consumed. More serious effects can be unconsciousness, alcohol poisoning, coma or death with excessive alcohol consumption. It's called a depressant for a reason.</p>
	<p>Longer-term effects of heavy drinking can leave you with serious health problems. These can include alcohol dependence, liver damage or disease, mood changes, cancer (mouth, throat, breast), sexual difficulties, memory loss or strokes.</p>
</section>

<section>
	<h3>Other Effects of Alcohol</h3>
	<p>Alcohol can affect more than just your body. There are physical and social risks that may happen when drinking too much. These can include injury, car crashes, getting into trouble with the police, arguments, fights, unwanted or unsafe sexual activity, offending others or doing things you regret later. Controlling your drinks enables you to control your behaviour.</p>
</section>

<section>
	<h3>Alcohol and the Law</h3>
	<p>Alcohol impairs your judgement, which can lead to doing something you wouldn't when sober. Some alcohol-related laws to keep in mind are:</p>
	<ul>
		<li>Consuming alcohol in a public place or on private property could result in a $500 fine and may even lead to a jail term of up to 60 days;</li>
		<li>Argumentative, disorderly or violent behavior isn't acceptable, it can get you into trouble with the police;</li>
	</ul>
</section>

<section>
	<h3>Driving Under the Influence (DUI)</h3>
	<p>DUI is taken very seriously. Here are the BAC limits for drivers of different ages and the hefty punishments that may result if you decide to take the risk:</p>
	<ul>
		<li>Drivers under the age of 21 - Your BAC limit for driving is 0.02%</li>
		<li>Other drivers - Your BAC limit is 0.08%</li>
		<li>If you are found to have driven under the influence, for a first offense you could get a fine between $500-$2000, plus administrative and potentially lawyer's fees. Even the first offense could lead to a sentence of 6-9 months in jail or an alcohol/drug treatment program plus probation, 50 hours or more of mandatory community service and leave you without a license for 6 months-1 year. Subsequent offenses and DUIs where bodily harm is involved increase the amount of fines and increase the likelihood of actual jail time rather than community service only.</li>
	</ul>
	<p>Drinking alcohol can be part of an enjoyable night out, but drinking safely will ensure you have good memories and that you keep your license.</p>
</section>
