<section>
	<h2>Stuff other people do...</h2>
</section>

<section>
	<h3>Flock together</h3>
	<p>There are more reasons to stick with your friends than just to laugh at them when they start stumbling. Looking out for them (and they for you) can ensure that the night ends on a good note and not with someone left on the side of the road or unconscious in a toilet stall.</p>
</section>

<section>
	<h3>Cashed up?</h3>
	<p>Two-minute noodle dinners can get pretty boring after the second week - but then that might be the only option after a big night out! Carrying less money with you when you go out reduces the amount of alcohol you purchase which can be good for your body as well as your pocket. Make sure you leave some aside though if you need to catch a taxi - try putting it in your shoe so you're less likely to spend it.</p>
</section>

<section>
	<h3>How many have you had?</h3>
	<p>Do you know what a standard drink is? It's a measurement of alcohol and it isn't always the same as a bottle/can/glass. Most drinks are more than a single 'standard drink' so you could be consuming more than you think. Safe drinking guidelines are based on this measurement so it pays to know what they are. Set a limit at the start of the night and stick to it. Avoid partial drink refills so you can actually keep track.</p>
</section>

<section>
	<h3>Slow down</h3>
	<p>Extend your night - space your drinks with water or soft drinks. It'll keep you hydrated and less likely to end up on the floor. Even start off your night with a non-alcoholic drink to quench your thirst before moving onto the booze. Grab a bite to eat beforehand and snack throughout the night.</p>
</section>

<section>
	<h3>Be yourself</h3>
	<p>If you don't feel like another drink or want to drink at your own pace, real friends should respect that. If you're getting hassled, tell them you're driving, on antibiotics, or you're a sexual athlete who needs to perform later that night. And soft drinks look the same whether there's alcohol in them or not. Find something that works for you.</p>
</section>

<section>
	<h3>Can I buy you a drink?</h3>
	<p>There might be some strangers you want to meet, but it's probably better to do it while you're conscious. Drink spiking happens - to girls and guys. To protect yourself, only accept drinks from people you don't know if they are unopened cans or bottles. Never leave your drink unattended and keep a thumb over the top of your bottle - it'll also help prevent you wasting your drink if your arms get a little too excited.</p>
</section>

<section>
	<h3>Unwanted gifts</h3>
	<p>Getting lucky doesn't mean you have to get Chlamydia. If there's any possibility that you may end up in bed with someone - carry a condom with you - and make sure you know how to use it.</p>
</section>

<section>
	<h3>Stay off the road</h3>
	<p>Plan how you're getting home before you go out. Grab a taxi - go in a group and you can share the bill. Put the taxi number in your phone. If you're organising a designated driver, make sure they really are not drinking - the last thing you want is to get to home-time with no money and no one is able to drive.</p>
</section>

<section>
	<h3>Mixers</h3>
	<p>Diversity might be the spice of life but when it comes to drugs it can be fatal. Mixing alcohol with other drugs - even prescribed medication can have unanticipated effects. Stick to the alcohol and if you're sick take the night off.</p>
</section>
