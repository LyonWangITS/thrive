	<h2>Stuff other people do...</h2>
</section>

<section>
	<h3>How many have you had?</h3>
	<p>Do you know what a standard drink is? It's a measurement of alcohol and it isn't always the same as a bottle/can/glass. Most drinks are more than a single 'standard drink' so you could be consuming more than you think. Safe drinking guidelines are based on this measurement so it pays to know what they are. Avoid partial drink refills so you can actually keep track of the number of drinks you've had in the course of a night.</p>
</section>

<section>
	<h3>Stick to your guns</h3>
	<p>Set a drink limit at the start of the night and stick to it.</p>
</section>

<section>
	<h3>Slow down</h3>
	<p>Extend your night. Space out your drinks.</p>
</section>

<section>
	<h3>Take turns</h3>
	<p>Alternate between alcoholic drinks and either water, juice or soft drinks. It'll keep you hydrated and less likely to end up on the floor. Even start off your night with a non-alcoholic drink to quench your thirst before moving onto the booze.</p>
</section>
