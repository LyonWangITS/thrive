	<h2>Stuff other people do...</h2>
</section>

<section>
	<h3>Flock together</h3>
	<p>There are more reasons to stick with your friends then just to laugh at them when they start stumbling. Looking out for them (and they for you) can ensure that the night ends on a good note and not with someone left on the side of the road or unconscious in a toilet stall.</p>
</section>

<section>
	<h3>Unwanted gifts</h3>
	<p>Getting lucky doesn't mean you have to get Chlamydia. If there's any possibility that you may end up in bed with someone - carry a condom with you - and make sure you know how to use it.</p>
</section>

<section>
	<h3>Stay off the road</h3>
	<p>Plan how you're getting home before you go out. Grab a taxi - go in a group and you can share the bill. Put the taxi number in your phone.</p>
</section>

<section>
	<h3>How to designate a driver</h3>
	<p>If you're organizing a designated driver, make sure they really are not drinking - the last thing you want is to get to home-time with no money and no one is able to drive.</p>
</section>
